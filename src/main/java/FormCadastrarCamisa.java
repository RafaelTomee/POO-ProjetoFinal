    public void cadastrarCamisa(){
        camisa = new Camisa();
        
        try{
        try{
        camisa.setTamanho(ctTamanho.getText());
        }catch(TamanhoErradoException tee){
            tee.impTamanhoErrado();
            ctTamanho.setText("");
            ctTamanho.requestFocus();
            return;
        }
        
        try{
        camisa.setCor(ctCor.getText());
        }catch(CorErradaException cee){
            cee.impCorErrada();
            ctCor.setText("");
            ctCor.requestFocus();
            return;
        }
        
        try{        
        camisa.setMarca(ctMarca.getText());
        }catch(MarcaErradaException mee){
            mee.impMarcaErrada();
            ctMarca.setText("");
            ctMarca.requestFocus();
            return;
        }
        
        try{            
        camisa.setTipoManga(ctManga.getText());
        }catch(MangaErradaException mee){
            mee.impMangaErrada();
            ctManga.setText("");
            ctManga.requestFocus();
            return;
        }
        
        try{
        camisa.setEstampa(ctEstampa.getText());
        }catch(EstampaErradaException eee){
            eee.impEstampaErrada();
            ctEstampa.setText("");
            ctEstampa.requestFocus();
            return;
        }
        try{
        camisa.getMaterial().setTipo(ctMaterial.getText());
        } catch(TipoErradoException tee){
            tee.impTipoErrado();
            ctMaterial.setText("");
            ctMaterial.requestFocus();
            return;
        }
        try{    
        camisa.getMaterial().setCuidados(ctCuidados.getText());
        }catch(CuidadosErradosException cee){
            cee.impCuidadosErrados();
            ctCuidados.setText("");
            ctCuidados.requestFocus();
            return;
        }
        camisa = estoque.cadCamisa(camisa);
        if(camisa != null){
            JOptionPane.showMessageDialog(null,"Camisa cadastrada com sucesso!", "Cadastro OK", 1);
            limpar();
        }

        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar a camisa!", "Cadastro ERRO", 0);
        }
    }
    
