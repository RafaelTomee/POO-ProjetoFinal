public void listarTab(){
        DefaultTableModel modelo = (DefaultTableModel) tbCamisa.getModel();
        int posLin = 0;
        modelo.setRowCount(posLin);
        
        for(Camisa camisa : estoque.getBdCamisa()){
            modelo.insertRow(posLin, new Object []{camisa.getTamanho(),
                                                   camisa.getCor(),
                                                   camisa.getMarca(),
                                                   camisa.getTipoManga(),
                                                   camisa.getEstampa(), 
                                                   camisa.getMaterial().getTipo(), 
                                                   camisa.getMaterial().getCuidados()});
            posLin++;
        }
    }    

