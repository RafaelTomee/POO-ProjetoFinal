public void remover(){
    Camisa camisaConsulta = new Camisa();
    
    // Definindo os atributos da camisa com base nos campos de texto
    try {
        camisaConsulta.setTamanho(ctTamanho.getText());
    } catch (TamanhoErradoException tee) {
        tee.impTamanhoErrado();
        return;
    }
    
    try {
        camisaConsulta.setCor(ctCor.getText());
    } catch (CorErradaException cee) {
        cee.impCorErrada();
        return;
    }
    
    try {
        camisaConsulta.setMarca(ctMarca.getText());
    } catch (MarcaErradaException mee) {
        mee.impMarcaErrada();
        return;
    }
    
    try {
        camisaConsulta.setTipoManga(ctManga.getText());
    } catch (MangaErradaException mee) {
        mee.impMangaErrada();
        return;
    }
    
    try {
        camisaConsulta.setEstampa(ctEstampa.getText());
    } catch (EstampaErradaException eee) {
        eee.impEstampaErrada();
        return;
    }
    
    try {
        camisaConsulta.getMaterial().setTipo(ctMaterial.getText());
    } catch (TipoErradoException tee) {
        tee.impTipoErrado();
        return;
    }
    
    try {
        camisaConsulta.getMaterial().setCuidados(ctCuidados.getText());
    } catch(CuidadosErradosException cee) {
        cee.impCuidadosErrados();
        return;
    }
    
    
    
    
    
    
    if (camisaConsulta != null) {
        ctTamanho.setText(camisaConsulta.getTamanho());
        ctCor.setText(camisaConsulta.getCor());
        ctMarca.setText(camisaConsulta.getMarca());
        ctManga.setText(camisaConsulta.getTipoManga());
        ctEstampa.setText(camisaConsulta.getEstampa());
        int x = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir essa Camisa?", "Confirmacao de exclusao",JOptionPane.YES_NO_CANCEL_OPTION);
        if(x == 0){
            estoque.removeCamisa(camisaConsulta);
            JOptionPane.showMessageDialog(null, "Camisa excluida com sucesso", "Exclusao de Camisa", 1);
        limpar();
        }
    } else {
        JOptionPane.showMessageDialog(null, "Nao existe Camisa com esses dados", "", JOptionPane.YES_NO_CANCEL_OPTION);
        limpar();
    }
}
   
    
