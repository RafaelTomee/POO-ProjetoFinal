    public void listarTab(){
        DefaultTableModel modelo = (DefaultTableModel) tbCamisa.getModel();
        int posLin = 0;
        modelo.setRowCount(posLin);
        
        for(Calca calca : estoque.getBdCalca()){
            modelo.insertRow(posLin, new Object []{calca.getTamanho(),
                                                   calca.getCor(),
                                                   calca.getMarca(),
                                                   calca.getTipoCintura(),
                                                   calca.getComprimento(), 
                                                   calca.getMaterial().getTipo(), 
                                                   calca.getMaterial().getCuidados()});
            posLin++;
        }
    }
    
