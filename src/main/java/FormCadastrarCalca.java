    public void remover(){
    Calca calcaConsulta = new Calca();
    
    // Definindo os atributos da camisa com base nos campos de texto
    try {
        calcaConsulta.setTamanho(ctTamanho.getText());
    } catch (TamanhoErradoException tee) {
        tee.impTamanhoErrado();
        return;
    }
    
    try {
        calcaConsulta.setCor(ctCor.getText());
    } catch (CorErradaException cee) {
        cee.impCorErrada();
        return;
    }
    
    try {
        calcaConsulta.setMarca(ctMarca.getText());
    } catch (MarcaErradaException mee) {
        mee.impMarcaErrada();
        return;
    }
    
    try {
        calcaConsulta.setTipoCintura(ctCintura.getText());
    } catch (CinturaErradaException mee) {
        mee.impCinturaErrada();
        return;
    }
    
    try {
        calcaConsulta.setComprimento(ctComprimento.getText());
    } catch (ComprimentoErradoException eee) {
        eee.impComprimentoErrado();
        return;
    }
    
    try {
        calcaConsulta.getMaterial().setTipo(ctMaterial.getText());
    } catch (TipoErradoException tee) {
        tee.impTipoErrado();
        return;
    }
    
    try {
        calcaConsulta.getMaterial().setCuidados(ctCuidados.getText());
    } catch(CuidadosErradosException cee) {
        cee.impCuidadosErrados();
        return;
    }
    
    
    
    
    
    
    if (calcaConsulta != null) {
        ctTamanho.setText(calcaConsulta.getTamanho());
        ctCor.setText(calcaConsulta.getCor());
        ctMarca.setText(calcaConsulta.getMarca());
        ctCintura.setText(calcaConsulta.getTipoCintura());
        ctComprimento.setText(calcaConsulta.getComprimento());
        int x = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir essa Calca?", "Confirmacao de exclusao",JOptionPane.YES_NO_CANCEL_OPTION);
        if(x == 0){
            estoque.removeCalca(calcaConsulta);
            JOptionPane.showMessageDialog(null, "Calca excluida com sucesso", "Exclusao de Calca", 1);
        limpar();
        }
    } else {
        JOptionPane.showMessageDialog(null, "Nao existe Calca com esses dados", "", JOptionPane.YES_NO_CANCEL_OPTION);
        limpar();
    }
}
    
