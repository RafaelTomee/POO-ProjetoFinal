    public void cadastrarCalca(){
        calca = new Calca();
        
        
        try{
        calca.setTamanho(ctTamanho.getText());
        }catch(TamanhoErradoException tee){
            tee.impTamanhoErrado();
            ctTamanho.setText("");
            ctTamanho.requestFocus();
            return;
        }
        
        try{
        calca.setCor(ctCor.getText());
        }catch(CorErradaException cee){
            cee.impCorErrada();
            ctCor.setText("");
            ctCor.requestFocus();
            return;
        }
        
        try{        
        calca.setMarca(ctMarca.getText());
        }catch(MarcaErradaException mee){
            mee.impMarcaErrada();
            ctMarca.setText("");
            ctMarca.requestFocus();
            return;
        }
        
        try{            
        calca.setTipoCintura(ctCintura.getText());
        }catch(CinturaErradaException mee){
            mee.impCinturaErrada();
            ctCintura.setText("");
            ctCintura.requestFocus();
            return;
        }
        
        try{
        calca.setComprimento(ctComprimento.getText());
        }catch(ComprimentoErradoException eee){
            eee.impComprimentoErrado();
            ctComprimento.setText("");
            ctComprimento.requestFocus();
            return;
        }
        try{
        calca.getMaterial().setTipo(ctMaterial.getText());
        } catch(TipoErradoException tee){
            tee.impTipoErrado();
            ctMaterial.setText("");
            ctMaterial.requestFocus();
            return;
        }
        try{    
        calca.getMaterial().setCuidados(ctCuidados.getText());
        }catch(CuidadosErradosException cee){
            cee.impCuidadosErrados();
            ctCuidados.setText("");
            ctCuidados.requestFocus();
            return;
        }
        calca = estoque.cadCalca(calca);
        if(calca != null){
            JOptionPane.showMessageDialog(null,"Calca cadastrada com sucesso!", "Cadastro OK", 1);
            limpar();
        }

        
        
    }
    
